def snapshot_nonzero_mask(df: pd.DataFrame, columns: Iterable[str]) -> tuple[list[str], np.ndarray]:
    """Capture a boolean mask of non-zero values before a zeroing operation."""
    tracked_columns = [col for col in columns if col in df.columns]
    if not tracked_columns:
        return [], np.empty((len(df), 0), dtype=bool)
    values = df.loc[:, tracked_columns].to_numpy(copy=False)
    return tracked_columns, np.isfinite(values) & (values != 0)

def record_zeroing_step_metrics(
    step_prefix: str,
    before_nonzero_mask: np.ndarray,
    tracked_columns: list[str],
    after_df: pd.DataFrame,
) -> None:
    """
    Record how many rows and values became zero after a zeroing operation.
    """
    rows_metric = f"{step_prefix}_rows_affected_pct"
    values_metric = f"{step_prefix}_values_zeroed_pct"

    if not tracked_columns or before_nonzero_mask.size == 0:
        filter_metrics[rows_metric] = 0.0
        filter_metrics[values_metric] = 0.0
        print(f"[filter-metrics] {step_prefix}: no tracked columns available.")
        return

    after_values = after_df.loc[:, tracked_columns].to_numpy(copy=False)
    n_rows = min(before_nonzero_mask.shape[0], after_values.shape[0])
    if n_rows == 0:
        filter_metrics[rows_metric] = 0.0
        filter_metrics[values_metric] = 0.0
        print(f"[filter-metrics] {step_prefix}: no rows available.")
        return

    before_nonzero = before_nonzero_mask[:n_rows]
    after_zero = np.isfinite(after_values[:n_rows]) & (after_values[:n_rows] == 0)
    newly_zero = before_nonzero & after_zero

    rows_affected = int(np.count_nonzero(np.any(newly_zero, axis=1)))
    values_zeroed = int(np.count_nonzero(newly_zero))
    total_values = n_rows * len(tracked_columns)

    record_activity_metric(rows_metric, rows_affected, n_rows, label="rows affected")
    record_activity_metric(values_metric, values_zeroed, total_values, label="values zeroed")

def compute_t_sum_spread(df_subset: pd.DataFrame, columns: list[str]) -> pd.Series:
    """Compute per-event spread max(non-zero) - min(non-zero) using vectorized ops."""
    if not columns:
        return pd.Series(np.nan, index=df_subset.index, dtype=float)
    values = df_subset.loc[:, columns].to_numpy(dtype=float, copy=False)
    values = np.where(values != 0, values, np.nan)
    valid_rows = np.any(~np.isnan(values), axis=1)
    spread = np.full(values.shape[0], np.nan, dtype=float)
    if np.any(valid_rows):
        valid_values = values[valid_rows]
        spread[valid_rows] = np.nanmax(valid_values, axis=1) - np.nanmin(valid_values, axis=1)
    return pd.Series(spread, index=df_subset.index)

def zero_outlier_tsum_columns(df_input: pd.DataFrame, columns: list[str], threshold: float) -> pd.DataFrame:
    """Zero T_sum outliers around the row-wise median using vectorized masking."""
    t_sum_cols = [col for col in columns if col in df_input.columns]
    if not t_sum_cols:
        return df_input
    values = df_input.loc[:, t_sum_cols].to_numpy(dtype=float, copy=True)
    nonzero_mask = values != 0
    enough_values_mask = nonzero_mask.sum(axis=1) >= 2
    masked_values = np.where(nonzero_mask, values, np.nan)
    centers = np.full(values.shape[0], np.nan, dtype=float)
    if np.any(enough_values_mask):
        centers[enough_values_mask] = np.nanmedian(masked_values[enough_values_mask], axis=1)
    deviations = np.abs(values - centers[:, None])
    outlier_mask = nonzero_mask & (deviations > (threshold / 2.0)) & enough_values_mask[:, None]
    values[outlier_mask] = 0.0
    df_input.loc[:, t_sum_cols] = values
    return df_input

def zero_strip_component_blocks(df: pd.DataFrame, self_trigger_mode: bool = False) -> None:
    """Zero per-strip Q/T components together when any component in the block is zero."""
    strip_labels: list[tuple[int, int]] = []
    q_sum_cols: list[str] = []
    q_diff_cols: list[str] = []
    t_sum_cols: list[str] = []
    t_diff_cols: list[str] = []

    for plane in range(1, 5):
        for strip in range(1, 5):
            q_sum = f"Q{plane}_Q_sum_{strip}"
            q_diff = f"Q{plane}_Q_dif_{strip}"
            t_sum = f"T{plane}_T_sum_{strip}"
            t_diff = f"T{plane}_T_dif_{strip}"
            if all(col in df.columns for col in (q_sum, q_diff, t_sum, t_diff)):
                strip_labels.append((plane, strip))
                q_sum_cols.append(q_sum)
                q_diff_cols.append(q_diff)
                t_sum_cols.append(t_sum)
                t_diff_cols.append(t_diff)

    if not strip_labels:
        return

    q_sum_values = df.loc[:, q_sum_cols].to_numpy(copy=True)
    q_diff_values = df.loc[:, q_diff_cols].to_numpy(copy=True)
    t_sum_values = df.loc[:, t_sum_cols].to_numpy(copy=True)
    t_diff_values = df.loc[:, t_diff_cols].to_numpy(copy=True)

    any_zero = (
        (q_sum_values == 0)
        | (q_diff_values == 0)
        | (t_sum_values == 0)
        | (t_diff_values == 0)
    )
    all_zero = (
        (q_sum_values == 0)
        & (q_diff_values == 0)
        & (t_sum_values == 0)
        & (t_diff_values == 0)
    )

    total_events = len(df)
    for idx, (plane, strip) in enumerate(strip_labels):
        num_affected_events = int(np.count_nonzero(any_zero[:, idx]))
        if self_trigger_mode:
            print(
                f"SELF TRIGGER. Plane {plane}, Strip {strip}: {num_affected_events} out of {total_events} events affected ({(num_affected_events / total_events) * 100:.2f}%)"
            )
        else:
            zeroes_before = int(np.count_nonzero(all_zero[:, idx]))
            set_to_zero = num_affected_events - zeroes_before
            print(
                f"Plane {plane}, Strip {strip}: {set_to_zero} out of {total_events} events affected ({(set_to_zero / total_events) * 100:.2f}%)"
            )

    q_sum_values[any_zero] = 0
    q_diff_values[any_zero] = 0
    t_sum_values[any_zero] = 0
    t_diff_values[any_zero] = 0

    df.loc[:, q_sum_cols] = q_sum_values
    df.loc[:, q_diff_cols] = q_diff_values
    df.loc[:, t_sum_cols] = t_sum_values
    df.loc[:, t_diff_cols] = t_diff_values

