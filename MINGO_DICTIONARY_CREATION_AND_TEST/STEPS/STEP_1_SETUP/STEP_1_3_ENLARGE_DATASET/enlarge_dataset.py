def _load_config(path: Path) -> dict:
    def _merge_dicts(base: dict, override: dict) -> dict:
        out = dict(base)
        for k, v in override.items():
            if isinstance(v, dict) and isinstance(out.get(k), dict):
                out[k] = _merge_dicts(out[k], v)
            else:
                out[k] = v
        return out

    cfg: dict = {}
    if path.exists():
        cfg = json.loads(path.read_text(encoding="utf-8"))
    runtime_path = path.with_name("config_runtime.json")
    if runtime_path.exists():
        runtime_cfg = json.loads(runtime_path.read_text(encoding="utf-8"))
        cfg = _merge_dicts(cfg, runtime_cfg)
        log.info("Loaded runtime overrides: %s", runtime_path)
    return cfg


def _as_bool(value: object, default: bool = False) -> bool:
    if isinstance(value, bool):
        return value
    if value is None:
        return bool(default)
    if isinstance(value, (int, float)):
        return value != 0
    return str(value).strip().lower() in {"1", "true", "yes", "y", "on"}


def _safe_float(value: object, default: float) -> float:
    try:
        out = float(value)
    except (TypeError, ValueError):
        return float(default)
    if np.isfinite(out):
        return out
    return float(default)


def _safe_int(value: object, default: int, minimum: int | None = None) -> int:
    try:
        out = int(value)
    except (TypeError, ValueError):
        out = int(default)
    if minimum is not None:
        out = max(int(minimum), out)
    return out


def _resolve_input_path(path_like: str | Path) -> Path:
    """Resolve path relative to project pipeline when not absolute."""
    p = Path(path_like).expanduser()
    if p.is_absolute():
        return p
    candidate_project = PROJECT_DIR / p
    if candidate_project.exists():
        return candidate_project
    candidate_pipeline = PIPELINE_DIR / p
    if candidate_pipeline.exists():
        return candidate_pipeline
    candidate_step = STEP_DIR / p
    if candidate_step.exists():
        return candidate_step
    return candidate_project


def _load_module(path: Path, name: str) -> ModuleType:
    if not path.exists():
        raise FileNotFoundError(f"Module path not found: {path}")
    spec = importlib.util.spec_from_file_location(name, str(path))
    if spec is None or spec.loader is None:
        raise RuntimeError(f"Could not create module spec for {path}")
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


def _pick_cfg(cfg_13: dict, cfg_31: dict, key: str, default: object) -> object:
    """Prefer STEP 1.3 config value; fallback to STEP 3.1 then default."""
    if key in cfg_13 and cfg_13.get(key) is not None:
        return cfg_13.get(key)
    if key in cfg_31 and cfg_31.get(key) is not None:
        return cfg_31.get(key)
    return default


def _merge_weighting_cfg(cfg_32: dict, cfg_13: dict) -> dict:
    """Build STEP 3.2-style weighting config for STEP 1.3 generation.

    Priority:
    1. internal defaults,
    2. `step_1_3.step_3_2_weighting` (centralized weighting block),
    3. `step_1_3.weighting_overrides` (final explicit overrides).
    """
    raw_cfg_32 = dict(cfg_32) if isinstance(cfg_32, dict) else {}
    ignored_step32_weighting_keys = sorted(
        key for key in raw_cfg_32.keys() if key in STEP32_WEIGHTING_KEYS
    )
    if ignored_step32_weighting_keys:
        log.info(
            "Ignoring %d weighting key(s) from step_3_2; using step_1_3.step_3_2_weighting + internal defaults.",
            len(ignored_step32_weighting_keys),
        )

    merged = dict(STEP32_WEIGHTING_DEFAULTS)
    central_weighting = cfg_13.get("step_3_2_weighting", {})
    if isinstance(central_weighting, dict):
        for key, value in central_weighting.items():
            if value is not None:
                merged[key] = value
    overrides = cfg_13.get("weighting_overrides", {})
    if isinstance(overrides, dict):
        for key, value in overrides.items():
            if value is not None:
                merged[key] = value
    return merged


def _resolve_target_new_rows(cfg_13: dict, n_base: int) -> int:
    """Resolve number of rows to generate."""
    raw_n = cfg_13.get("n_new_rows", None)
    if raw_n not in (None, "", "null", "None"):
        try:
            return max(0, int(raw_n))
        except (TypeError, ValueError):
            return 0
    try:
        multiplier = float(cfg_13.get("size_multiplier", 2.0))
    except (TypeError, ValueError):
        multiplier = 2.0
    if not np.isfinite(multiplier):
        multiplier = 2.0
    return max(0, int(np.rint((multiplier - 1.0) * float(n_base))))


def _build_uniform_targets(
    *,
    step31: ModuleType,
    source_df: pd.DataFrame,
    rate_df: pd.DataFrame,
    cfg_13: dict,
    cfg_31: dict,
    n_new_rows: int,
) -> tuple[pd.DataFrame, dict]:
    """Build near-uniform target points in (flux, eff) for dataset enlargement."""
    flux_col = str(_pick_cfg(cfg_13, cfg_31, "flux_column", "flux_cm2_min"))
    eff_pref = str(_pick_cfg(cfg_13, cfg_31, "eff_column", "eff_sim_1"))

    rate_col = str(_pick_cfg(cfg_13, cfg_31, "rate_column", "events_per_second_global_rate"))
    rate_flux_col = str(_pick_cfg(cfg_13, cfg_31, "rate_flux_column", flux_col))
    rate_eff_cfg = str(_pick_cfg(cfg_13, cfg_31, "rate_eff_column", eff_pref))
    if rate_col not in rate_df.columns:
        raise KeyError(f"Rate column '{rate_col}' not found in rate table.")
    if rate_flux_col not in rate_df.columns:
        rate_flux_col = flux_col
    if rate_flux_col not in rate_df.columns:
        raise KeyError(f"Rate-flux column '{rate_flux_col}' not found in dictionary table.")
    rate_eff_col = rate_eff_cfg if rate_eff_cfg in rate_df.columns else step31._choose_eff_column(rate_df, rate_eff_cfg)
    rate_model = step31._build_rate_model(rate_df, rate_flux_col, rate_eff_col, rate_col)

    dict_flux = pd.to_numeric(rate_df[rate_flux_col], errors="coerce")
    dict_eff = pd.to_numeric(rate_df[rate_eff_col], errors="coerce")
    valid_dict = dict_flux.notna() & dict_eff.notna()
    if int(valid_dict.sum()) < 3:
        raise ValueError(
            f"Not enough valid dictionary rows for bounds using {rate_flux_col}/{rate_eff_col}."
        )
    dict_flux_min = float(dict_flux[valid_dict].min())
    dict_flux_max = float(dict_flux[valid_dict].max())
    dict_eff_min = float(dict_eff[valid_dict].min())
    dict_eff_max = float(dict_eff[valid_dict].max())

    # STEP 1.3 limits are anchored to dictionary bounds.
    flux_range = (dict_flux_min, dict_flux_max)
    eff_range = (dict_eff_min, dict_eff_max)

    flux_range_cfg = cfg_13.get("flux_range", None)
    if flux_range_cfg not in (None, "", "null", "None"):
        req = step31._resolve_numeric_range(flux_range_cfg, dict_flux_min, dict_flux_max)
        lo = max(dict_flux_min, float(min(req)))
        hi = min(dict_flux_max, float(max(req)))
        if hi > lo:
            flux_range = (lo, hi)
        else:
            log.warning(
                "step_1_3.flux_range is outside dictionary bounds; using full dictionary range."
            )

    eff_range_cfg = cfg_13.get("eff_range", None)
    if eff_range_cfg not in (None, "", "null", "None"):
        req = step31._resolve_numeric_range(eff_range_cfg, dict_eff_min, dict_eff_max)
        lo = max(dict_eff_min, float(min(req)))
        hi = min(dict_eff_max, float(max(req)))
        if hi > lo:
            eff_range = (lo, hi)
        else:
            log.warning(
                "step_1_3.eff_range is outside dictionary bounds; using full dictionary range."
            )

    seed = step31._resolve_seed({"random_seed": cfg_13.get("random_seed", None)})
    rng = np.random.default_rng(seed)

    flux_lo = float(min(flux_range))
    flux_hi = float(max(flux_range))
    eff_lo = float(min(eff_range))
    eff_hi = float(max(eff_range))

    n_side = max(1, int(np.ceil(np.sqrt(float(max(1, n_new_rows))))))
    all_cells = np.arange(n_side * n_side, dtype=int)
    rng.shuffle(all_cells)
    chosen_cells = all_cells[:n_new_rows]
    ix = chosen_cells % n_side
    iy = chosen_cells // n_side

    jitter_fraction = _safe_float(cfg_13.get("uniform_jitter_fraction", 1.0), 1.0)
    jitter_fraction = float(np.clip(jitter_fraction, 0.0, 1.0))
    if jitter_fraction <= 0.0:
        ux = np.full(n_new_rows, 0.5, dtype=float)
        uy = np.full(n_new_rows, 0.5, dtype=float)
    else:
        ux = np.clip(0.5 + (rng.random(n_new_rows) - 0.5) * jitter_fraction, 0.0, 1.0)
        uy = np.clip(0.5 + (rng.random(n_new_rows) - 0.5) * jitter_fraction, 0.0, 1.0)

    if np.isclose(flux_lo, flux_hi, atol=0.0):
        target_flux = np.full(n_new_rows, flux_lo, dtype=float)
    else:
        flux_step = (flux_hi - flux_lo) / float(n_side)
        target_flux = flux_lo + (ix.astype(float) + ux) * flux_step
        target_flux = np.clip(target_flux, flux_lo, flux_hi)

    if np.isclose(eff_lo, eff_hi, atol=0.0):
        target_eff = np.full(n_new_rows, eff_lo, dtype=float)
    else:
        eff_step = (eff_hi - eff_lo) / float(n_side)
        target_eff = eff_lo + (iy.astype(float) + uy) * eff_step
        target_eff = np.clip(target_eff, eff_lo, eff_hi)

    target_rate_hz = np.asarray(
        step31._predict_rate(rate_model, target_flux, target_eff, min_rate_hz=1e-6),
        dtype=float,
    )
    if not np.isfinite(target_rate_hz).all():
        raise ValueError("Could not compute finite global-rate values at uniform target points.")
    target_rate_hz = np.maximum(target_rate_hz, 1e-6)

    source_events_finite = np.array([], dtype=float)
    if "n_events" in source_df.columns:
        source_events_finite = pd.to_numeric(source_df["n_events"], errors="coerce").dropna().to_numpy(dtype=float)
    default_events_per_file = 50000
    if source_events_finite.size:
        default_events_per_file = int(max(1.0, float(np.median(source_events_finite))))

    events_cfg = cfg_13.get("events_per_file", None)
    events_cfg_set = events_cfg not in (None, "", "null", "None")
    sample_events_default = not events_cfg_set
    sample_n_events = _as_bool(
        cfg_13.get("sample_n_events_from_base", sample_events_default),
        sample_events_default,
    )

    events_constant: int | None = None
    if events_cfg_set:
        events_constant = _safe_int(events_cfg, default_events_per_file, minimum=1)
        n_events = np.full(n_new_rows, events_constant, dtype=int)
        events_mode = "constant_from_events_per_file"
    elif sample_n_events and source_events_finite.size:
        sampled = rng.choice(source_events_finite, size=n_new_rows, replace=True)
        n_events = np.clip(np.rint(sampled).astype(int), 1, None)
        events_mode = "sampled_from_base_dataset"
    else:
        n_events = np.full(n_new_rows, default_events_per_file, dtype=int)
        events_mode = "constant_from_base_median"

    duration_seconds = np.divide(
        n_events.astype(float),
        target_rate_hz,
        out=np.full(n_new_rows, 1.0, dtype=float),
        where=target_rate_hz > 0.0,
    )
    duration_seconds = np.maximum(duration_seconds, 1e-6)

    elapsed_end = np.cumsum(duration_seconds)
    elapsed_start = np.concatenate([[0.0], elapsed_end[:-1]])
    elapsed_mid = 0.5 * (elapsed_start + elapsed_end)

    start_time_raw = str(_pick_cfg(cfg_13, cfg_31, "start_time_utc", "2026-01-01T00:00:00Z"))
    start_time = pd.to_datetime(start_time_raw, utc=True, errors="coerce")
    if pd.isna(start_time):
        start_time = pd.Timestamp("2026-01-01T00:00:00Z")

    out = pd.DataFrame({
        "file_index": np.arange(1, n_new_rows + 1, dtype=int),
        "elapsed_seconds_start": elapsed_start,
        "elapsed_seconds_end": elapsed_end,
        "elapsed_seconds": elapsed_mid,
        "elapsed_hours_start": elapsed_start / 3600.0,
        "elapsed_hours_end": elapsed_end / 3600.0,
        "elapsed_hours": elapsed_mid / 3600.0,
        "duration_seconds": duration_seconds,
        "n_events_expected": n_events.astype(float),
        "n_events": n_events,
        "target_events_per_file": (
            np.full(n_new_rows, float(events_constant), dtype=float)
            if events_constant is not None
            else np.full(n_new_rows, np.nan, dtype=float)
        ),
        "global_rate_hz_mid": target_rate_hz,
        "global_rate_hz_mean": target_rate_hz,
        "flux": target_flux,
        "eff": target_eff,
    })
    out["flux_cm2_min"] = out["flux"]
    out["eff_sim_1"] = out["eff"]

    time_start = start_time + pd.to_timedelta(out["elapsed_seconds_start"], unit="s")
    time_end = start_time + pd.to_timedelta(out["elapsed_seconds_end"], unit="s")
    time_mid = start_time + pd.to_timedelta(out["elapsed_seconds"], unit="s")
    out["time_start_utc"] = pd.Series(time_start).dt.strftime("%Y-%m-%dT%H:%M:%SZ")
    out["time_end_utc"] = pd.Series(time_end).dt.strftime("%Y-%m-%dT%H:%M:%SZ")
    out["time_utc"] = pd.Series(time_mid).dt.strftime("%Y-%m-%dT%H:%M:%SZ")

    info = {
        "target_generation_mode": "uniform_density_grid_jitter",
        "seed_used": int(seed),
        "flux_column": flux_col,
        "eff_column_source": eff_pref,
        "dictionary_bounds_flux": [dict_flux_min, dict_flux_max],
        "dictionary_bounds_eff": [dict_eff_min, dict_eff_max],
        "flux_range_used": [float(flux_range[0]), float(flux_range[1])],
        "eff_range_used": [float(eff_range[0]), float(eff_range[1])],
        "rate_column": rate_col,
        "rate_flux_column": rate_flux_col,
        "rate_eff_column": rate_eff_col,
        "uniform_grid_side": int(n_side),
        "uniform_jitter_fraction": float(jitter_fraction),
        "n_events_mode": events_mode,
        "events_per_file_constant": int(events_constant) if events_constant is not None else None,
        "duration_hours_effective": float(elapsed_end[-1] / 3600.0) if len(elapsed_end) else 0.0,
        "global_rate_hz_range": [float(np.min(target_rate_hz)), float(np.max(target_rate_hz))],
    }
    return out.reset_index(drop=True), info


def _generate_synthetic_rows(
    *,
    step32: ModuleType,
    cfg_weight: dict,
    base_dataset_df: pd.DataFrame,
    dictionary_df: pd.DataFrame,
    time_df: pd.DataFrame,
    flux_col: str,
    eff_pref: str,
) -> tuple[pd.DataFrame, dict]:
    """Generate synthetic rows from targets using STEP 3.2 internals."""
    basis_source = str(cfg_weight.get("basis_source", "dataset")).strip().lower()
    if basis_source == "dictionary":
        basis_input_df = dictionary_df
    elif basis_source == "dataset":
        basis_input_df = base_dataset_df
    else:
        raise ValueError(f"Invalid basis_source '{basis_source}'. Use 'dataset' or 'dictionary'.")

    if flux_col not in time_df.columns or flux_col not in basis_input_df.columns:
        raise KeyError(f"Flux column '{flux_col}' must exist in time targets and basis.")
    eff_col_time = step32._choose_eff_column(time_df, eff_pref)
    eff_col_basis = step32._choose_eff_column(basis_input_df, eff_pref)

    time_events_col = str(cfg_weight.get("time_n_events_column", "n_events"))
    time_rate_col = str(cfg_weight.get("time_rate_column", "global_rate_hz_mean"))
    time_duration_col = str(cfg_weight.get("time_duration_column", "duration_seconds"))
    for required_col in (time_events_col, time_rate_col, time_duration_col):
        if required_col not in time_df.columns:
            raise KeyError(f"Target time-series column '{required_col}' not found.")

    basis_events_col = str(cfg_weight.get("basis_n_events_column", "n_events"))
    basis_events_tol_pct = step32._safe_float(
        cfg_weight.get("basis_n_events_tolerance_pct", cfg_weight.get("basis_n_events_tolerance", 30.0)),
        30.0,
    )
    basis_parameter_set_col_cfg = cfg_weight.get("basis_parameter_set_column", None)
    basis_min_rows = step32._safe_int(cfg_weight.get("basis_min_rows", 1), 1, minimum=1)

    basis_flux_all = pd.to_numeric(basis_input_df[flux_col], errors="coerce").to_numpy(dtype=float)
    basis_eff_all = pd.to_numeric(basis_input_df[eff_col_basis], errors="coerce").to_numpy(dtype=float)
    basis_events_all = None
    if basis_events_col in basis_input_df.columns:
        basis_events_all = pd.to_numeric(basis_input_df[basis_events_col], errors="coerce").to_numpy(dtype=float)

    target_flux = pd.to_numeric(time_df[flux_col], errors="coerce").to_numpy(dtype=float)
    target_eff = pd.to_numeric(time_df[eff_col_time], errors="coerce").to_numpy(dtype=float)
    target_events = pd.to_numeric(time_df[time_events_col], errors="coerce").to_numpy(dtype=float)
    valid_basis = np.isfinite(basis_flux_all) & np.isfinite(basis_eff_all)
    valid_target = np.isfinite(target_flux) & np.isfinite(target_eff)
    if not np.any(valid_basis):
        raise ValueError("No valid basis points in selected basis table.")
    if not np.all(valid_target):
        raise ValueError("Generated targets contain invalid flux/eff points.")

    basis_work = basis_input_df.loc[valid_basis].reset_index(drop=True)
    basis_flux = basis_flux_all[valid_basis]
    basis_eff = basis_eff_all[valid_basis]
    basis_events = None if basis_events_all is None else basis_events_all[valid_basis]

    basis_parameter_set_col = step32._select_parameter_set_column(basis_work, basis_parameter_set_col_cfg)
    if basis_parameter_set_col is None:
        parameter_set_values = np.asarray([f"row_{i}" for i in range(len(basis_work))], dtype=object)
    else:
        parameter_set_values = basis_work[basis_parameter_set_col].astype(str).to_numpy(dtype=object)

    one_per_set_mask, one_per_set_info = step32._build_one_per_parameter_set_mask(
        parameter_set_values=parameter_set_values,
        basis_events=basis_events,
        target_events=target_events,
        basis_flux=basis_flux,
        basis_eff=basis_eff,
        target_flux=target_flux,
        target_eff=target_eff,
    )
    event_mask_extra, extra_info = step32._build_event_mask(
        basis_events=basis_events,
        target_events=target_events,
        tolerance_pct=basis_events_tol_pct,
        min_rows=basis_min_rows,
    )
    event_mask = one_per_set_mask if event_mask_extra is None else (one_per_set_mask & event_mask_extra)

    flux_span = max(float(np.nanmax(basis_flux) - np.nanmin(basis_flux)), 1e-9)
    eff_span = max(float(np.nanmax(basis_eff) - np.nanmin(basis_eff)), 1e-9)
    sigma_flux = step32._safe_float(
        cfg_weight.get("distance_sigma_flux_abs"),
        step32._safe_float(cfg_weight.get("distance_sigma_flux_fraction", 0.10), 0.10) * flux_span,
    )
    sigma_eff = step32._safe_float(
        cfg_weight.get("distance_sigma_eff_abs"),
        step32._safe_float(cfg_weight.get("distance_sigma_eff_fraction", 0.10), 0.10) * eff_span,
    )
    method = str(cfg_weight.get("weighting_method", "gaussian"))
    top_k_raw = cfg_weight.get("top_k", None)
    top_k = None if top_k_raw in (None, "", 0) else step32._safe_int(top_k_raw, 8, minimum=1)
    distance_hardness = step32._safe_float(cfg_weight.get("distance_hardness", 1.0), 1.0)

    density_enabled = step32._safe_bool(cfg_weight.get("density_correction_enabled", True), True)
    density_scaling = None
    if density_enabled:
        density_k = step32._safe_int(cfg_weight.get("density_correction_k_neighbors", 10), 10, minimum=1)
        density_exp = step32._safe_float(cfg_weight.get("density_correction_exponent", 1.0), 1.0)
        density_clip_min = step32._safe_float(cfg_weight.get("density_correction_clip_min", 0.25), 0.25)
        density_clip_max = step32._safe_float(cfg_weight.get("density_correction_clip_max", 4.0), 4.0)
        if density_clip_max < density_clip_min:
            density_clip_max = density_clip_min
        density_scaling, _ = step32._compute_inverse_density_scaling(
            basis_flux=basis_flux,
            basis_eff=basis_eff,
            k_neighbors=density_k,
            exponent=density_exp,
            clip_min=density_clip_min,
            clip_max=density_clip_max,
        )

    weights = step32._build_weights(
        dict_flux=basis_flux,
        dict_eff=basis_eff,
        target_flux=target_flux,
        target_eff=target_eff,
        method=method,
        sigma_flux=sigma_flux,
        sigma_eff=sigma_eff,
        top_k=top_k,
        distance_hardness=distance_hardness,
        density_scaling=density_scaling,
        event_mask=event_mask,
    )

    synthetic_df, dominant_idx = step32._make_synthetic_dataset(
        dictionary_df=basis_work,
        template_df=base_dataset_df,
        time_df=time_df,
        weights=weights,
        flux_col=flux_col,
        eff_col=eff_col_time,
        time_rate_col=time_rate_col,
        time_events_col=time_events_col,
        time_duration_col=time_duration_col,
        flux_output_values=target_flux,
        eff_output_values=target_eff,
    )

    info = {
        "basis_source": basis_source,
        "basis_rows_total": int(len(basis_input_df)),
        "basis_rows_valid_flux_eff": int(len(basis_work)),
        "basis_parameter_set_column": basis_parameter_set_col if basis_parameter_set_col is not None else "__row_index__",
        "one_per_set_mode": str(one_per_set_info.get("mode", "unknown")),
        "event_tolerance_info": extra_info,
        "sigma_flux": float(sigma_flux),
        "sigma_eff": float(sigma_eff),
        "weighting_method": method,
        "distance_hardness": float(distance_hardness),
        "top_k": int(top_k) if top_k is not None else None,
        "density_correction_enabled": bool(density_enabled),
        "dominant_basis_unique_count": int(len(np.unique(dominant_idx))),
    }
    return synthetic_df, info


def _plot_flux_eff_overlay(
    *,
    original_df: pd.DataFrame,
    synthetic_df: pd.DataFrame,
    flux_col: str,
    eff_col: str,
    path: Path,
) -> None:
    fig, ax = plt.subplots(figsize=(8.5, 6.8))
    ox = pd.to_numeric(original_df.get(flux_col), errors="coerce")
    oy = pd.to_numeric(original_df.get(eff_col), errors="coerce")
    sx = pd.to_numeric(synthetic_df.get(flux_col), errors="coerce")
    sy = pd.to_numeric(synthetic_df.get(eff_col), errors="coerce")
    m_o = ox.notna() & oy.notna()
    m_s = sx.notna() & sy.notna()
    if m_o.any():
        ax.scatter(
            ox[m_o], oy[m_o],
            s=10, color="#4C78A8", alpha=0.25, linewidths=0.0, label=f"Original (n={int(m_o.sum())})"
        )
    if m_s.any():
        ax.scatter(
            sx[m_s], sy[m_s],
            s=24, facecolors="white", edgecolors="#D62728", linewidths=0.8, alpha=0.95,
            label=f"Added synthetic (n={int(m_s.sum())})",
        )
    ax.set_xlabel(flux_col)
    ax.set_ylabel(eff_col)
    ax.set_title("STEP 1.3 enlargement in flux-eff plane")
    ax.grid(True, alpha=0.25)
    ax.legend(loc="best", fontsize=8)
    fig.tight_layout()
    _save_figure(fig, path, dpi=180)
    plt.close(fig)


def _plot_events_overlay(
    *,
    original_df: pd.DataFrame,
    synthetic_df: pd.DataFrame,
    path: Path,
) -> None:
    fig, ax = plt.subplots(figsize=(8.5, 5.5))
    o = pd.to_numeric(original_df.get("n_events"), errors="coerce").dropna()
    s = pd.to_numeric(synthetic_df.get("n_events"), errors="coerce").dropna()
    if o.empty and s.empty:
        plt.close(fig)
        return
    combined = pd.concat([o, s], ignore_index=True)
    lo, hi = float(combined.min()), float(combined.max())
    if hi <= lo:
        hi = lo + 1.0
    bins = np.linspace(lo, hi, 45)
    if not o.empty:
        ax.hist(o, bins=bins, color="#4C78A8", alpha=0.45, edgecolor="white", linewidth=0.4, label=f"Original (n={len(o)})")
    if not s.empty:
        ax.hist(s, bins=bins, color="#E45756", alpha=0.50, edgecolor="white", linewidth=0.4, label=f"Added synthetic (n={len(s)})")
    ax.set_xlabel("n_events")
    ax.set_ylabel("Count")
    ax.set_title("STEP 1.3: n_events distribution (original vs added)")
    ax.grid(True, alpha=0.25)
    ax.legend(loc="best", fontsize=8)
    fig.tight_layout()
    _save_figure(fig, path, dpi=180)
    plt.close(fig)


def _plot_iso_rate_global_rate(
    *,
    enlarged_df: pd.DataFrame,
    flux_col: str,
    eff_pref: str,
    path: Path,
) -> None:
    """STEP 1.3 iso-rate plot analogous to STEP 1.2 iso_rate_global_rate."""
    from matplotlib.tri import Triangulation, LinearTriInterpolator

    rate_col = "events_per_second_global_rate"
    if rate_col not in enlarged_df.columns:
        for candidate in ("global_rate_hz_source", "global_rate_hz_mean", "global_rate_hz_mid"):
            if candidate in enlarged_df.columns:
                rate_col = candidate
                break
    if rate_col not in enlarged_df.columns:
        log.warning("No global-rate column available in enlarged dataset — skipping iso-rate plot.")
        return

    if flux_col not in enlarged_df.columns:
        log.warning("Flux column '%s' not found in enlarged dataset — skipping iso-rate plot.", flux_col)
        return

    eff_series = None
    if eff_pref in enlarged_df.columns:
        eff_series = pd.to_numeric(enlarged_df[eff_pref], errors="coerce")
    elif "eff_sim_1" in enlarged_df.columns:
        eff_series = pd.to_numeric(enlarged_df["eff_sim_1"], errors="coerce")
    elif "efficiencies" in enlarged_df.columns:
        try:
            eff_series = (
                enlarged_df["efficiencies"]
                .apply(lambda v: ast.literal_eval(v) if isinstance(v, str) else v)
                .apply(lambda x: float(x[0]) if isinstance(x, (list, tuple)) and len(x) >= 1 else np.nan)
            )
        except Exception:
            eff_series = None
    if eff_series is None:
        log.warning("No efficiency column available for iso-rate plot — skipping.")
        return

    flux = pd.to_numeric(enlarged_df[flux_col], errors="coerce")
    eff = pd.to_numeric(eff_series, errors="coerce")
    rate = pd.to_numeric(enlarged_df[rate_col], errors="coerce")
    xm = flux.to_numpy(dtype=float)
    ym = eff.to_numpy(dtype=float)
    zm = rate.to_numpy(dtype=float)
    mask = np.isfinite(xm) & np.isfinite(ym) & np.isfinite(zm)
    if int(mask.sum()) < 10:
        log.warning("Too few valid points for STEP 1.3 iso-rate plot.")
        return
    xm, ym, zm = xm[mask], ym[mask], zm[mask]

    flux_lo = float(np.min(xm))
    flux_hi = float(np.max(xm))
    eff_lo = float(np.min(ym))
    eff_hi = float(np.max(ym))
    x_span = max(flux_hi - flux_lo, 1e-6)
    y_span = max(eff_hi - eff_lo, 1e-6)
    flux_lo -= 0.03 * x_span
    flux_hi += 0.03 * x_span
    eff_lo -= 0.03 * y_span
    eff_hi += 0.03 * y_span

    g = 220
    xi = np.linspace(flux_lo, flux_hi, g)
    yi = np.linspace(eff_lo, eff_hi, g)
    Xi, Yi = np.meshgrid(xi, yi)

    cmap = plt.cm.viridis
    fig, ax = plt.subplots(figsize=(9.5, 7.0))
    try:
        tri = Triangulation(xm, ym)
        interp = LinearTriInterpolator(tri, zm)
        Zi = interp(Xi, Yi)
        Zi = np.asarray(np.ma.filled(Zi, np.nan), dtype=float)
        finite_z = Zi[np.isfinite(Zi)]
        if finite_z.size >= 2:
            levels = np.linspace(float(np.min(finite_z)), float(np.max(finite_z)), 16)
            cf = ax.contourf(Xi, Yi, Zi, levels=levels, cmap=cmap, alpha=0.35, zorder=0)
            cbar = fig.colorbar(cf, ax=ax, pad=0.02, fraction=0.048)
            cbar.set_label("Global rate [Hz]")
            ax.contour(
                Xi,
                Yi,
                Zi,
                levels=levels[::2],
                colors="k",
                linewidths=0.35,
                alpha=0.25,
                zorder=1,
            )
        else:
            levels = np.linspace(float(np.min(zm)), float(np.max(zm)), 8)
            sc = ax.scatter(
                xm,
                ym,
                c=zm,
                cmap=cmap,
                s=20,
                alpha=0.7,
                edgecolors="0.35",
                linewidths=0.3,
                zorder=2,
                vmin=levels.min(),
                vmax=levels.max(),
            )
            cbar = fig.colorbar(sc, ax=ax, pad=0.02, fraction=0.048)
            cbar.set_label("Global rate [Hz]")
    except Exception as exc:
        log.warning("STEP 1.3 iso-rate contour interpolation failed: %s", exc)
        levels = np.linspace(float(np.min(zm)), float(np.max(zm)), 8)
        sc = ax.scatter(
            xm,
            ym,
            c=zm,
            cmap=cmap,
            s=20,
            alpha=0.7,
            edgecolors="0.35",
            linewidths=0.3,
            zorder=2,
            vmin=levels.min(),
            vmax=levels.max(),
        )
        cbar = fig.colorbar(sc, ax=ax, pad=0.02, fraction=0.048)
        cbar.set_label("Global rate [Hz]")

    syn_mask = None
    if "is_enlarged_synthetic_point" in enlarged_df.columns:
        syn_all = pd.Series(enlarged_df["is_enlarged_synthetic_point"]).fillna(False).astype(bool).to_numpy()
        syn_mask = syn_all[mask]
    if syn_mask is not None:
        ref_mask = ~syn_mask
        if np.any(ref_mask):
            ax.scatter(
                xm[ref_mask],
                ym[ref_mask],
                s=10,
                alpha=0.18,
                color="#606060",
                zorder=1,
                label="Original points",
            )
        if np.any(syn_mask):
            ax.scatter(
                xm[syn_mask],
                ym[syn_mask],
                s=26,
                facecolor="white",
                edgecolor="black",
                linewidth=0.6,
                zorder=3,
                label="Added synthetic points",
            )
    else:
        ax.scatter(
            xm,
            ym,
            s=26,
            facecolor="white",
            edgecolor="black",
            linewidth=0.6,
            zorder=3,
            label="Enlarged points",
        )

    ax.set_xlim(flux_lo, flux_hi)
    ax.set_ylim(eff_lo, eff_hi)
    ax.set_xlabel("Flux [cm^-2 min^-1]", fontsize=11)
    ax.set_ylabel("Efficiency (plane 1)", fontsize=11)
    ax.set_title(f"Iso-global-rate map ({int(mask.sum())} enlarged entries)", fontsize=12)
    ax.grid(True, alpha=0.2)
    ax.legend(loc="best", fontsize=8)
    fig.tight_layout()
    _save_figure(fig, path, dpi=150)
    plt.close(fig)


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Step 1.3: Enlarge STEP 1.2 dataset with uniform-density target sampling."
    )
    parser.add_argument("--config", default=str(DEFAULT_CONFIG))
    parser.add_argument("--base-dataset-csv", default=None)
    parser.add_argument("--dictionary-csv", default=None)
    args = parser.parse_args()

    config = _load_config(Path(args.config))
    _clear_plots_dir()

    cfg_13 = config.get("step_1_3", {})
    cfg_31 = config.get("step_3_1", {})
    cfg_32 = config.get("step_3_2", {})

    enabled = _as_bool(cfg_13.get("enabled", False), False)
    base_dataset_path = _resolve_input_path(
        args.base_dataset_csv
        or cfg_13.get("base_dataset_csv")
        or DEFAULT_BASE_DATASET
    )
    dictionary_path = _resolve_input_path(
        args.dictionary_csv
        or cfg_13.get("dictionary_csv")
        or cfg_32.get("dictionary_csv")
        or DEFAULT_DICTIONARY
    )

    if not base_dataset_path.exists():
        log.error("Base dataset CSV not found: %s", base_dataset_path)
        return 1
    if not dictionary_path.exists():
        log.error("Dictionary CSV not found: %s", dictionary_path)
        return 1

    base_df = pd.read_csv(base_dataset_path, low_memory=False)
    dict_df = pd.read_csv(dictionary_path, low_memory=False)
    if base_df.empty:
        log.error("Base dataset CSV is empty: %s", base_dataset_path)
        return 1
    if dict_df.empty:
        log.error("Dictionary CSV is empty: %s", dictionary_path)
        return 1

    summary: dict[str, object] = {
        "enabled": bool(enabled),
        "base_dataset_csv": str(base_dataset_path),
        "dictionary_csv": str(dictionary_path),
        "base_rows": int(len(base_df)),
    }
    if not enabled:
        summary["status"] = "skipped_disabled"
        out_summary = FILES_DIR / "enlargement_summary.json"
        with open(out_summary, "w", encoding="utf-8") as f:
            json.dump(summary, f, indent=2)
        log.info("step_1_3.enabled=false -> skipping enlargement.")
        log.info("Wrote summary: %s", out_summary)
        return 0

    n_new_rows = _resolve_target_new_rows(cfg_13, len(base_df))
    if n_new_rows <= 0:
        summary["status"] = "skipped_zero_new_rows"
        summary["n_new_rows_requested"] = int(n_new_rows)
        out_summary = FILES_DIR / "enlargement_summary.json"
        with open(out_summary, "w", encoding="utf-8") as f:
            json.dump(summary, f, indent=2)
        log.warning("Requested n_new_rows resolved to %d; no enlargement generated.", n_new_rows)
        log.info("Wrote summary: %s", out_summary)
        return 0

    try:
        step31 = _load_module(STEP31_PATH, "step31_create_time_series")
        step32 = _load_module(STEP32_PATH, "step32_synthetic_time_series")
    except Exception as exc:
        log.error("Could not load STEP 3 helper modules: %s", exc)
        return 1

    try:
        time_df, ts_info = _build_uniform_targets(
            step31=step31,
            source_df=base_df,
            rate_df=dict_df,
            cfg_13=cfg_13,
            cfg_31=cfg_31,
            n_new_rows=n_new_rows,
        )
    except Exception as exc:
        log.error("Failed to build uniform-density enlargement targets: %s", exc)
        return 1

    flux_col = str(ts_info.get("flux_column", "flux_cm2_min"))
    eff_pref = str(_pick_cfg(cfg_13, cfg_31, "eff_column", "eff_sim_1"))
    cfg_weight = _merge_weighting_cfg(cfg_32, cfg_13)

    try:
        synthetic_only_df, synth_info = _generate_synthetic_rows(
            step32=step32,
            cfg_weight=cfg_weight,
            base_dataset_df=base_df,
            dictionary_df=dict_df,
            time_df=time_df,
            flux_col=flux_col,
            eff_pref=eff_pref,
        )
    except Exception as exc:
        log.error("Failed to synthesize enlargement rows with STEP 3.2 logic: %s", exc)
        return 1

    original_tag = "step_1_2_original"
    synthetic_tag = "step_1_3_enlarged"
    source_col = "dataset_source"
    base_out = base_df.copy()
    synth_out = synthetic_only_df.copy()
    base_out[source_col] = original_tag
    synth_out[source_col] = synthetic_tag
    synth_out["is_enlarged_synthetic_point"] = True
    base_out["is_enlarged_synthetic_point"] = False

    enlarged_df = pd.concat([base_out, synth_out], ignore_index=True, sort=False)

    out_time = FILES_DIR / "enlargement_time_series.csv"
    out_only = FILES_DIR / "enlarged_only_dataset.csv"
    out_full = FILES_DIR / "enlarged_dataset.csv"
    out_summary = FILES_DIR / "enlargement_summary.json"
    time_df.to_csv(out_time, index=False)
    synth_out.to_csv(out_only, index=False)
    enlarged_df.to_csv(out_full, index=False)

    eff_for_plot = eff_pref if eff_pref in enlarged_df.columns else "eff_sim_1"
    if eff_for_plot not in enlarged_df.columns:
        for candidate in ("eff_sim_1", "eff_sim_2", "eff_sim_3", "eff_sim_4"):
            if candidate in enlarged_df.columns:
                eff_for_plot = candidate
                break
    if flux_col in enlarged_df.columns and eff_for_plot in enlarged_df.columns:
        _plot_flux_eff_overlay(
            original_df=base_out,
            synthetic_df=synth_out,
            flux_col=flux_col,
            eff_col=eff_for_plot,
            path=PLOTS_DIR / "flux_eff_original_vs_enlarged.png",
        )
    _plot_events_overlay(
        original_df=base_out,
        synthetic_df=synth_out,
        path=PLOTS_DIR / "events_original_vs_enlarged.png",
    )
    _plot_iso_rate_global_rate(
        enlarged_df=enlarged_df,
        flux_col=flux_col,
        eff_pref=eff_for_plot,
        path=PLOTS_DIR / "iso_rate_global_rate.png",
    )

    summary.update({
        "status": "ok",
        "n_new_rows_requested": int(n_new_rows),
        "n_new_rows_generated": int(len(synth_out)),
        "n_rows_enlarged_dataset": int(len(enlarged_df)),
        "output_enlarged_only_csv": str(out_only),
        "output_enlarged_csv": str(out_full),
        "output_enlargement_time_series_csv": str(out_time),
        "time_series_generation": ts_info,
        "weighting_generation": synth_info,
    })
    with open(out_summary, "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    log.info("Base dataset rows: %d", len(base_df))
    log.info("Added synthetic rows: %d", len(synth_out))
    log.info("Enlarged dataset rows: %d", len(enlarged_df))
    log.info("Wrote synthetic-only dataset: %s", out_only)
    log.info("Wrote enlarged dataset: %s", out_full)
    log.info("Wrote target time series: %s", out_time)
    log.info("Wrote summary: %s", out_summary)
    log.info("Done.")
    return 0


